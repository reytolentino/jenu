class AW_Onestepcheckout_Block_Adminhtml_System_Config_Form_Related_Enabled
    extends Mage_Adminhtml_Block_System_Config_Form_Field
{
    protected function _getElementHtml(Varien_Data_Form_Element_Abstract $element)
    {
        $isAutomaticRelatedEnabled = (int)Mage::helper('aw_onestepcheckout/related')->isAutomaticRelatedEnabled();
        $element->setValue($isAutomaticRelatedEnabled);
        $element->setScope(null);
        return parent::_getElementHtml($element);
    }
}